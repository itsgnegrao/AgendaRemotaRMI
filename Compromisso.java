public class Compromisso {
    String Data;
    String Hora;
    String Assunto;
    String Descricao;
    String Notificacao;

    public Compromisso(String Data, String Hora, String Assunto, String Descricao, String Notificacao) {
        this.Data = Data;
        this.Hora = Hora;
        this.Assunto = Assunto;
        this.Descricao = Descricao;
        this.Notificacao = Notificacao;
    }

}
